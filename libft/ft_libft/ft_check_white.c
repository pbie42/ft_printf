#include "libftprintf.h"

int						ft_check_white(char *s)
{
	int					i;

	i = -1;
	while (s[++i])
		if (s[i] == ' ' || s[i] == '\t' || s[i] == '\r' || s[i] == '\v'
		|| s[i] == '\f' || s[i] == '\n')
			return (1);
	return (0);
}
