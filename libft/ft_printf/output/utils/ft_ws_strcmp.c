#include "libftprintf.h"

int			ft_ws_strcmp(const wchar_t *s1, const wchar_t *s2)
{
	unsigned int	i;

	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i])
		i++;
	return ((wchar_t)s1[i] - (wchar_t)s2[i]);
}
